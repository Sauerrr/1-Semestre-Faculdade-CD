valor_quadrado = float(input("Digite um valor: ")) #essa linha possui uma variavel fixa
valor_cubo = float(input("Digite mais um valor: "))

diferenca = (valor_quadrado ** 2) - (valor_cubo ** 3)

print(f"A diferença é de: {diferenca}")

modulo = abs(diferenca)
print(f"O módulo é de: {modulo}")